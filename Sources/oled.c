void LCD_P6x8num(char x,char y,int num)
{
  unsigned char i=0,j=0;
  int n=0,num1=0,c=0,js=1,js1;
  if(num<0)
  {
    num=-num;
    num1=num;
    LCD_Set_Pos(x,y);    
  	for(i=0;i<6;i++)     
  	LCD_WrDat(F6x8[13][i]);
  	x=x+8; 
  }
  else
  {
    num1=num;
  }
  
  if(num==0) 
  {
    LCD_Set_Pos(x,y);    
  	for(i=0;i<6;i++)     
  	LCD_WrDat(F6x8[16][i]);
  }
  while(num1!=0) 
  {
    num1 = num1/10;
    n++;
  }
  for(js1=n-1;js1>0;js1--) 
  {
    js=js*10;
  }
  while (n!=0)
  { 
    c =num/js;
  	num=num-c*js;
  	c=c+16;
    if(x>126){x=0;y++;}
    LCD_Set_Pos(x,y);    
  	for(i=0;i<6;i++)     
  	LCD_WrDat(F6x8[c][i]); 
  	x+=6;
  	js=js/10;	
  	n--;
  }
  for(j=1;j>0;j--)
  {
    for(i=0;i<8;i++)     
  	LCD_WrDat(F6x8[0][i]);                                                         
  }
}
